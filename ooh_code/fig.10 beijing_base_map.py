def deg2num(lat_deg, lon_deg, zoom):
    lat_rad = math.radians(lat_deg)
    n = 2.0 ** zoom
    xtile = int((lon_deg + 180.0) / 360.0 * n)
    ytile = int((1.0 - math.asinh(math.tan(lat_rad)) / math.pi) / 2.0 * n)
    return (xtile, ytile)

def num2deg(xtile, ytile, zoom):
    n = 2.0 ** zoom
    lon_deg = xtile / n * 360.0 - 180.0
    lat_rad = math.atan(math.sinh(math.pi * (1 - 2 * ytile / n)))
    lat_deg = math.degrees(lat_rad)
    return (lat_deg, lon_deg)

def download_map(center_lat, center_lon, zoom, style, width_tiles, height_tiles):
    tile_servers = {
        "standard": "https://tile.openstreetmap.org/{z}/{x}/{y}.png",
        "carto_light": "https://a.basemaps.cartocdn.com/light_all/{z}/{x}/{y}.png",
        "carto_dark": "https://a.basemaps.cartocdn.com/dark_all/{z}/{x}/{y}.png",
    }
    tile_url = tile_servers.get(style, tile_servers["carto_light"])

    center_x, center_y = deg2num(center_lat, center_lon, zoom)
    x_min, x_max = center_x - width_tiles // 2, center_x + width_tiles // 2
    y_min, y_max = center_y - height_tiles // 2, center_y + height_tiles // 2

    width = (x_max - x_min + 1) * 256
    height = (y_max - y_min + 1) * 256
    result = Image.new('RGB', (width, height))

    headers = {'User-Agent': 'Mozilla/5.0', 'Referer': 'https://www.openstreetmap.org/'}
    total = (x_max - x_min + 1) * (y_max - y_min + 1)
    count = 0

    print(f"下载 {total} 块瓦片...")
    for x in range(x_min, x_max + 1):
        for y in range(y_min, y_max + 1):
            url = tile_url.format(z=zoom, x=x, y=y)
            try:
                resp = requests.get(url, headers=headers, timeout=15, verify=False)
                if resp.status_code == 200:
                    tile = Image.open(BytesIO(resp.content))
                    result.paste(tile, ((x - x_min) * 256, (y - y_min) * 256))
                count += 1
                print(f"\r进度: {count}/{total}", end="", flush=True)
            except:
                pass
    print("\n完成!")

    lat_max, lon_min = num2deg(x_min, y_min, zoom)
    lat_min, lon_max = num2deg(x_max + 1, y_max + 1, zoom)
    return result, [lon_min, lon_max, lat_min, lat_max]

